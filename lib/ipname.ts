/** IP アドレスを SHA-256 でハッシュし、一意かつ決定論的な名前を返す。64×64=4096通り */
export function ipToName(ip: string): string {
  const hash = createHash("sha256").update(ip).digest();
  const adj = ADJECTIVES[hash[0] % ADJECTIVES.length];
  const noun = NOUNS[hash[1] % NOUNS.length];
  return `${adj}-${noun}`;
}
